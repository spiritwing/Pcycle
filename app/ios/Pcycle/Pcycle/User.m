#import "User.h"

@implementation User

@synthesize _userName;
@synthesize _userID;
@synthesize _userPhoneNumber;
@synthesize _userWeight;
@synthesize _userHeight;
@synthesize _userAge;
@synthesize _userMaxStamina;
@synthesize _userMaxStaminaTimeStamp;
@synthesize _userLastStamina;
@synthesize _userTotalMileage;
@synthesize _userFrontalArea;
@synthesize _userMaxSpeed;
@synthesize _userMaxSpeedTimeStamp;
@synthesize _userMaxPower;
@synthesize _userMaxPowerTimeStamp;





-(id) initUserName :(NSString *) uN
              UserID : (NSString *) userID
              UserPN : (NSString *) userPhoneNumber
              UserW : (float) userWeight
              UserH : (float) userHeight
              UserA : (int) userAge
              UserMStamina : (int) userMaxStamina
              UserMStaminaTS : (TIME_STAMP_TYPE) userMaxStaminaTimeStamp
              UserLS : (int) userLastStamina
              UserTM : (double) userTotalMileage
              UserFA : (float) userFrontalArea
              UserMSpeed : (float) userMaxSpeed
              UserMSpeedTS : (TIME_STAMP_TYPE) userMaxSpeedTimeStamp
              UserMP : (float) userMaxPower
              UserMPTS : (TIME_STAMP_TYPE) userMaxPowerTimeStamp
{
    if(self = [super init])
    {
        self._userName = uN;
        self._userID = userID;
        self._userPhoneNumber = userPhoneNumber;
        self._userWeight = userWeight;
        self._userHeight = userHeight;
        self._userAge = userAge;
        self._userMaxStamina = userMaxStamina;
        self._userMaxStaminaTimeStamp = userMaxStaminaTimeStamp;
        self._userLastStamina = userLastStamina;
        self._userTotalMileage = userTotalMileage;
        self._userFrontalArea = userFrontalArea;
        self._userMaxSpeed = userMaxSpeed;
        self._userMaxSpeedTimeStamp = userMaxSpeedTimeStamp;
        self._userMaxPower = userMaxPower;
        self._userMaxPowerTimeStamp = userMaxPowerTimeStamp;
    }
    return self;
}


@end

